    private void carregarPoupancaDobanco() {
        UsuarioDAO dao = new UsuarioDAO();
        double[] dados = dao.buscarPoupanca(nomeUsuario);
        if (dados != null) {
            double poupancaMensal = dados[0];
            double taxaJurosAnual = dados[1];
            double acumulado      = dados[2];
            TelaPrevisao.setAcumuladoPoupanca(acumulado);
            String poupancaFormatada = String.format("%,.2f", poupancaMensal);
            String jurosFormatado    = String.format("%.2f%%", taxaJurosAnual);
            txtOutputPoupanca.setText("Poupança mensal: R$ " + poupancaFormatada + " | Taxa anual: " + jurosFormatado);
            TelaPrevisao.setPoupancaMensal(poupancaMensal);
        }
    }
 
    private void salvarPoupanca() {
        String poupancaStr = txtPoupancaMensal.getText().trim();
        String jurosStr = txtTaxaJuros.getText().trim();
        
        if (poupancaStr.isEmpty() || jurosStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha ambos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            double poupancaMensal = Double.parseDouble(poupancaStr.replace(",", "."));
            double taxaJurosAnual = Double.parseDouble(jurosStr.replace(",", "."));
            
            Poupanca poupanca = new Poupanca();
            poupanca.configurarPoupanca(poupancaMensal, taxaJurosAnual);
            poupanca.projetarPoupanca();
            double acumuladoFinal = poupanca.getAcumulado();
            
            // Persiste no banco.
            UsuarioDAO dao = new UsuarioDAO();
            boolean sucesso = dao.salvarPoupanca(nomeUsuario, poupancaMensal, taxaJurosAnual, acumuladoFinal);
            
            if (!sucesso) {
                JOptionPane.showMessageDialog(this, "Erro ao salvar poupança no banco.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
 
            TelaPrevisao.setPoupancaMensal(poupancaMensal);
            TelaPrevisao.setAcumuladoPoupanca(acumuladoFinal);
            
            String poupancaFormatada = String.format("%,.2f", poupancaMensal);
            String jurosFormatado = String.format("%.2f%%", taxaJurosAnual);
            txtOutputPoupanca.setText("Poupança mensal: R$ " + poupancaFormatada + " | Taxa anual: " + jurosFormatado);
            
            txtPoupancaMensal.setText("");
            txtTaxaJuros.setText("");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valores inválidos. Use números (ex: 1500,50 ou 1500.50).", "Erro de formato", JOptionPane.ERROR_MESSAGE);
        }
    } 


