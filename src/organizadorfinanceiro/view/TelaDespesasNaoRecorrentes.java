    private void cadastrarDespesa() {
        String descricao = txtDespesa.getText().trim();
        String valorMensalStr = txtValorMensal.getText().trim();
        String qtdeParcelasStr = txtQtdeParcelas.getText().trim();
 
        if (descricao.isEmpty() || valorMensalStr.isEmpty() || qtdeParcelasStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
 
        try {
            double valorMensal = Double.parseDouble(valorMensalStr.replace(",", "."));
            byte qtdeParcelas = Byte.parseByte(qtdeParcelasStr);
 
            DespesasNaoRecorrentes despesa = new DespesasNaoRecorrentes(descricao, valorMensal, qtdeParcelas);
 
            // Persiste no banco.
            UsuarioDAO dao = new UsuarioDAO();
            boolean sucesso = dao.inserirDespesaNaoRecorrente(nomeUsuario, despesa);
 
            if (!sucesso) {
                JOptionPane.showMessageDialog(this, "Erro ao salvar no banco.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
 
            listaDespesas.add(despesa);
            atualizarTabela();
 
            txtOutItemCadastrado.setText("Despesa '" + descricao + "' cadastrada!");
            txtDespesa.setText("");
            txtValorMensal.setText("");
            txtQtdeParcelas.setText("");
            txtDespesa.requestFocus();
 
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valores devem ser números válidos (use vírgula ou ponto).", "Erro de formato", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void excluirDespesa() {
        int linhaSelecionada = tblDespesas.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma despesa na tabela para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
 
        int confirm = JOptionPane.showConfirmDialog(this, "Deseja excluir a despesa selecionada?", "Confirmar exclusão", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            // Exclui do banco
            UsuarioDAO dao = new UsuarioDAO();
            dao.excluirDespesaNaoRecorrente(nomeUsuario, linhaSelecionada);
 
            listaDespesas.remove(linhaSelecionada);
            atualizarTabela();
        }
    }
    
    // Método para sincronizar a tabela com a lista de objetos.
    private void atualizarTabela() {
        modeloTabela.setRowCount(0); // limpa a tabela
        
        for (DespesasNaoRecorrentes d : listaDespesas) {
            double valorTotal = d.calcularParcelas();
            modeloTabela.addRow(new Object[]{
                d.getDescricao(),
                d.getQtdeParcelas(),
                String.format("%,.2f", d.getValorMensal())
            });
        }
    }

